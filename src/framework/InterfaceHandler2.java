package framework;

public class InterfaceHandler2 {
	
	public String name;
	public String vendor;
	public String partnumber;
	public double price;
	public String interface_type_in;
	public String interface_type_out;
	public double toothcount;
	public double weight_lbs;

	
	public InterfaceHandler2(String name, String vendor, String partnumber,
			double price, String interface_type_in, String interface_type_out, double toothcount, double weight_lbs) {
		this.name = name;
		this.vendor = vendor;
		this.partnumber = partnumber;
		this.price = price;
		this.interface_type_in = interface_type_in;
		this.interface_type_in = interface_type_out;
		this.toothcount = toothcount;
		this.weight_lbs = weight_lbs;


	}
	
	
}
